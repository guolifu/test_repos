//--------------------------------------------------
// Description  : Adjust gamma
// Input Value  : Gamma table type and gamma tables
// Output Value : None
//--------------------------------------------------
void RTD_WriteGamma(BYTE *pGammaTableArrayR, BYTE *pGammaTableArrayG, BYTE *pGammaTableArrayB)
{
    CScalerSetByte(_GAMMA_CTRL_67, 0x81);
    CScalerWrite(_GAMMA_PORT_66, GAMMA_TALBE, pGammaTableArrayR, _NON_AUTOINC);
    CScalerSetByte(_GAMMA_CTRL_67, 0x91);
    CScalerWrite(_GAMMA_PORT_66, GAMMA_TALBE, pGammaTableArrayG, _NON_AUTOINC);
    CScalerSetByte(_GAMMA_CTRL_67, 0xa1);
    CScalerWrite(_GAMMA_PORT_66, GAMMA_TALBE, pGammaTableArrayB, _NON_AUTOINC);
    CScalerSetByte(_GAMMA_CTRL_67, 0x40);  //Enable Gamma 
}


void UpdateGammaBlock(BYTE gamma)
{
//cyc : It can't turn on gamma when push power key two times.
	if (cCurGamma == gamma)//&&(cCurModule == SystemData.Module)
		return;
	
	cCurGamma=gamma;
	
	//cCurModule=SystemData.Module;	

	
	if(cCurGamma == DefaultGammaFactor) //0
	{
		CScalerSetByte(_GAMMA_CTRL_67, 0x00);     //disenable Gamma 
//			RTD_WriteGamma(tTco03GammaTable_LPL[0],tTco03GammaTable_LPL[1],tTco03GammaTable_LPL[2]);
	}
	else 
	{
		if(cCurGamma == 0)	//-50                                                                                                                                                
			RTD_WriteGamma(tTco03GammaTable_LPL[3],tTco03GammaTable_LPL[4],tTco03GammaTable_LPL[5]);
	
		else	//+50	
			RTD_WriteGamma(tTco03GammaTable_LPL[6],tTco03GammaTable_LPL[7],tTco03GammaTable_LPL[8]);

	}
	
}

